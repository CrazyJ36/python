#!/usr/bin/env python3

# Like with for loops, the 'in' keyword itslef can be used
# as A boolean operator.

word = 'five'
print("word is 'five'\n")

if 'i' in word:
  print("there's an 'I' in word")

if not 'x' in word:
  print("there is no 'x' in word")

print("\n'f' in word?:")
print( 'f' in word )

